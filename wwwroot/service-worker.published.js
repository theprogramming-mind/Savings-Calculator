// service-worker.published.js
// This is the PRODUCTION service worker, used when you run `dotnet publish`.
// Blazor automatically replaces service-worker.js with this file after publishing.
//
// WHAT THIS FILE DOES:
// A service worker is essentially a background script that sits between your
// app and the network. When the browser requests a file, the service worker
// intercepts that request and can decide: "do I have this cached already?
// If yes, serve it instantly. If no, go fetch it from the network and cache it."
//
// This gives your Savings Calculator:
//   - Instant load times after the first visit (served from cache, not network)
//   - Offline support (works even with no internet connection)
//   - Installability (users can "install" it like a native app)

// The cache version name. IMPORTANT: Every time you publish a new version of
// your app, Blazor regenerates the service-worker-assets.js manifest with a
// new hash. The self.assetsManifest.version below will change automatically,
// which triggers the service worker to update and re-cache fresh files.
const cacheNamePrefix = 'offline-cache-';
const cacheName = `${cacheNamePrefix}${self.assetsManifest.version}`;

// These are the assets that MUST be cached for the app to work offline.
// The assetsManifest is auto-generated by Blazor at publish time and contains
// every file in your wwwroot (your .wasm files, JS, CSS, images, etc).
const offlineAssetsInclude = [
    /\.dll$/,        // .NET assemblies compiled to DLL
    /\.pdb$/,        // Debug symbols
    /\.wasm/,        // WebAssembly binaries (the core of Blazor WASM)
    /\.html/,        // HTML pages
    /\.js$/,         // JavaScript files
    /\.json$/,       // JSON files (config, appsettings, etc)
    /\.css$/,        // Stylesheets (including your compiled Tailwind CSS)
    /\.woff$/,       // Web fonts
    /\.woff2$/,      // Web fonts (modern format)
    /\.png$/,        // Images
    /\.ico$/,        // Favicon
];

// These are assets we intentionally DON'T cache — things that change
// too frequently or shouldn't be served stale.
const offlineAssetsExclude = [
    /^service-worker\.js$/  // Never cache the service worker itself!
];

// --- INSTALL EVENT ---
// Fires once when the service worker is first registered (or updated).
// This is where we pre-cache all the app's assets so it can work offline.
self.addEventListener('install', event => {
    event.waitUntil(onInstall(event));
});

// --- ACTIVATE EVENT ---
// Fires after the new service worker takes control.
// We use this to clean up any old caches from previous versions of the app.
self.addEventListener('activate', event => {
    event.waitUntil(onActivate(event));
});

// --- FETCH EVENT ---
// Fires every time the browser requests a resource (JS file, CSS, API call, etc).
// This is the core interception point — we decide: cache or network?
self.addEventListener('fetch', event => {
    event.respondWith(onFetch(event));
});

// INSTALL HANDLER
// Downloads and caches all assets listed in the Blazor-generated manifest.
async function onInstall(event) {
    console.info('[Service Worker] Installing Savings Calculator PWA...');

    // skipWaiting() means the new service worker activates immediately
    // instead of waiting for old tabs to close.
    self.skipWaiting();

    // Open our versioned cache and pre-cache all matching assets.
    const assetsRequests = self.assetsManifest.assets
        .filter(asset => offlineAssetsInclude.some(pattern => pattern.test(asset.url)))
        .filter(asset => !offlineAssetsExclude.some(pattern => pattern.test(asset.url)))
        .map(asset => new Request(asset.url, { integrity: asset.hash, cache: 'no-cache' }));
        // The `integrity` field is a subresource integrity hash — it ensures
        // the files haven't been tampered with or corrupted in transit.

    const cache = await caches.open(cacheName);
    await cache.addAll(assetsRequests);

    console.info('[Service Worker] All assets cached successfully.');
}

// ACTIVATE HANDLER
// Deletes any caches whose name starts with our prefix but isn't the current version.
// This prevents old cached files from taking up storage space forever.
async function onActivate(event) {
    const cacheKeys = await caches.keys();
    const oldCacheKeys = cacheKeys.filter(key =>
        key.startsWith(cacheNamePrefix) && key !== cacheName
    );

    await Promise.all(oldCacheKeys.map(key => {
        console.info(`[Service Worker] Deleting old cache: ${key}`);
        return caches.delete(key);
    }));

    // Take control of all open tabs immediately without requiring a page reload.
    await clients.claim();
}

// FETCH HANDLER
// Uses a "Cache First" strategy for app assets:
//   1. Check if the resource is in the cache.
//   2. If yes → return it instantly (fast, works offline).
//   3. If no → fetch from network, then cache it for next time.
// For non-asset requests (like API calls), we just pass through to the network.
async function onFetch(event) {
    let cachedResponse = null;

    // Only apply cache logic to GET requests — never cache POST/PUT/DELETE.
    if (event.request.method === 'GET') {
        const shouldServeIndexHtml = event.request.mode === 'navigate';
        // 'navigate' requests are page navigations (typing a URL, clicking a link).
        // For these, we always want to serve index.html so Blazor's router can handle them.

        const request = shouldServeIndexHtml ? 'index.html' : event.request;
        const cache = await caches.open(cacheName);
        cachedResponse = await cache.match(request);
    }

    // Return cached version if available, otherwise fall back to the network.
    return cachedResponse || fetch(event.request);
}
